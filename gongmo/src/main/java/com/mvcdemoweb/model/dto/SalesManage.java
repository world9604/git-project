package com.mvcdemoweb.model.dto;

import java.io.Serializable;
import java.sql.Date;

public class SalesManage implements Serializable {
	
	private int salesManageNo;
	private int memberNo;
	private int storeNo;
	private boolean storeStatue;
	private Date byDate;
	private int buyerNo;
	private int sellerNo;
	
	
	public int getSalesManageNo() {
		return salesManageNo;
	}
	public void setSalesManageNo(int salesManageNo) {
		this.salesManageNo = salesManageNo;
	}
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public int getStoreNo() {
		return storeNo;
	}
	public void setStoreNo(int storeNo) {
		this.storeNo = storeNo;
	}
	public boolean isStoreStatue() {
		return storeStatue;
	}
	public void setStoreStatue(boolean storeStatue) {
		this.storeStatue = storeStatue;
	}
	public Date getByDate() {
		return byDate;
	}
	public void setBydate(Date byDate) {
		this.byDate = byDate;
	}
	public int getBuyerNo() {
		return buyerNo;
	}
	public void setBuyerNo(int buyerNo) {
		this.buyerNo = buyerNo;
	}
	public int getSellerNo() {
		return sellerNo;
	}
	public void setSellerNo(int sellerNo) {
		this.sellerNo = sellerNo;
	}
	
}
