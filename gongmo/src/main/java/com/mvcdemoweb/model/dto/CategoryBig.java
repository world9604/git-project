package com.mvcdemoweb.model.dto;

import java.io.Serializable;
import java.util.List;

public class CategoryBig implements Serializable {
	
	private int bigNo; //대분류 번호
	private String bigName; // 대분류 명칭
	
	//1 대 다 관계
	private List<CategoryMiddle> middle;
	
	
	public int getBigNo() {
		return bigNo;
	}
	public void setBigNo(int bigNo) {
		this.bigNo = bigNo;
	}
	public String getBigName() {
		return bigName;
	}
	public void setBigName(String bigName) {
		this.bigName = bigName;
	}
	public List<CategoryMiddle> getMiddle() {
		return middle;
	}
	public void setMiddle(List<CategoryMiddle> middle) {
		this.middle = middle;
	}

	
	

}
