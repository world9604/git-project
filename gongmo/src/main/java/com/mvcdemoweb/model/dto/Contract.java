package com.mvcdemoweb.model.dto;

import java.io.Serializable;

public class Contract implements Serializable {
	
	private int contractNo;
	private String contractName;
	private String contractUploadPath;
	
	
	public int getContractNo() {
		return contractNo;
	}
	public void setContractNo(int contractNo) {
		this.contractNo = contractNo;
	}
	public String getContractName() {
		return contractName;
	}
	public void setContractName(String contractName) {
		this.contractName = contractName;
	}
	public String getContractUploadPath() {
		return contractUploadPath;
	}
	public void setContractUploadPath(String contractUploadPath) {
		this.contractUploadPath = contractUploadPath;
	}
	
	

}
